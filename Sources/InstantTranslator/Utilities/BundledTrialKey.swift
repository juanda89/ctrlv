import Foundation

enum BundledTrialKey {
    static func geminiKey() -> String {
        let obfuscated: [UInt8] = [
            0x22, 0x3d, 0x08, 0x0d, 0x25, 0x54, 0x30, 0x16, 0x1d, 0x54,
            0x5d, 0x68, 0x01, 0x09, 0x09, 0x25, 0x06, 0x18, 0x1d, 0x7c,
            0x77, 0x0a, 0x72, 0x50, 0x1a, 0x06, 0x02, 0x00, 0x6e, 0x41,
            0x25, 0x25, 0x18, 0x54, 0x77, 0x1d, 0x10, 0x54, 0x1e
        ]
        let salt: [UInt8] = [
            0x63, 0x74, 0x72, 0x6c, 0x76, 0x2d, 0x74, 0x72, 0x69, 0x61,
            0x6c, 0x2d, 0x67, 0x65, 0x6d, 0x69, 0x6e, 0x69, 0x2d, 0x32,
            0x30, 0x32, 0x35, 0x63, 0x74, 0x72, 0x6c, 0x76, 0x2d, 0x74,
            0x72, 0x69, 0x61, 0x6c, 0x2d, 0x67, 0x65, 0x6d, 0x69
        ]
        let decoded = zip(obfuscated, salt).map { $0 ^ $1 }
        return String(bytes: decoded, encoding: .utf8) ?? ""
    }
}
